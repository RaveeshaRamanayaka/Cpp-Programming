/* 
   "iostream" is a header file that contains functions for input/output operations ( cin and cout )
*/

#include <iostream>

// main function
int main(void) {
	
    // "cout" is a Output Statement
    std::cout << "Name\nRaveesha Ramanayaka\n";
    
    // "endl" use to add new line
    std::cout << "Name" << std::endl;
    std::cout << "Raveesha Ramanayaka" << std::endl;
    
    return 0;
}
